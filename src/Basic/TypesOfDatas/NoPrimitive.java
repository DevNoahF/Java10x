package Basic.TypesOfDatas;

public class NoPrimitive {
    public static void main(String[] args) {


    /*
    * Dados nao primitivos:
    *   - String
    *   - Array
    *   - Class
    *   - Enum
    * */
        String nome = "Naruto";
        String nomeUpperCase= nome.toUpperCase(); // Tudo em CAPSLOCK
        String aldeia = "Casa do krl";
        String aldeiaUpperCase = aldeia.toUpperCase();

        System.out.println("Penes " +nomeUpperCase);
        System.out.println(nome);
        System.out.println(aldeiaUpperCase);
        System.out.println(aldeia);


    }
}