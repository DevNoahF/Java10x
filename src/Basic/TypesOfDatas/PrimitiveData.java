package Basic.TypesOfDatas;

public class PrimitiveData {
    public static void main(String[] args) { //psvm = Public Static Void Main = BoilerPlate

        /* tabela de dados Primitivos:
        * Boolean:
        *   - Boolean
        * Caracteres:
        *   - Char
        * Inteiros(Interger):
        *   - Byte
        *   - Short
        *   - Int
        *   - Long
        * Ponto flutuante:
        *   - Float
        *   - Double
        * */

        int Idade = 16;
        double Altura = 1.70;
        char Inicial = 'N';
        boolean VivoOuMorto = true;
        long SaldoBancario = 1223L;


        System.out.println("Idade: " + Idade);
        System.out.println("Altura: " + Altura);
        System.out.println("Inicial: " + Inicial);
        System.out.println("Vivo Ou Morto:" +VivoOuMorto);
        System.out.println("Saldo Bancario: " + SaldoBancario);


    }
}
